public class ex11ImprimirNumerosDel1Al100 {

	public static void main(String[] args) {
		
		//Imprime los números del 1 al 100 usando un bucle for
		//En cada iteración el contador i aumenta en 1 y se imprime su valor.
		for (int i = 1; i <= 100; i++) {
			System.out.println(i);
		}
	}

}
